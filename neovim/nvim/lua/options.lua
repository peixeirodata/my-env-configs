local opt = vim.opt

--add numbered lines
opt.number = true

-- add relative line numbering
opt.relativenumber = true

--confirm to save changes before exiting buffer
opt.confirm = true

--size of identation
opt.shiftwidth = 4

--set number of spaces tabs count for
opt.tabstop = 4

-- true color support
opt.termguicolors = true

-- show invisible characteres
opt.list = true
